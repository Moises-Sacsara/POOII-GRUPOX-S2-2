    private void escribirArchivo(String nombreProducto, String cantidad) {
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("stock.txt", true))) {
            bw.write(nombreProducto + "," + cantidad);
            bw.newLine();
            JOptionPane.showMessageDialog(this, "Producto agregado correctamente.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al escribir en el archivo.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
